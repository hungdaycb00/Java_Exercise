package Lession16_2;

public class Point {
    private int x;
    private int y;

    public Point(int x, int y){
        this.x = x;
        this.y = y;
    }
    //getters and setters
    public int getX(){
        return x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int[] getXY() {
        return getXY();
    }
    public void setXY(){
        this.x = x;
        this.y = y;
    }

}

